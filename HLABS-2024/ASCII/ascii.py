def xor_decrypt(value, key):
    """Decrypts a value using XOR with the given key."""
    return value ^ key

def is_printable(string):
    """Checks if the string is printable."""
    return all(32 <= ord(char) <= 126 for char in string)

def main():
    flag_in_ascii = [
        0x97, 0x9a, 0x93, 0x9d, 0x84, 0xbe, 0xac, 0xbc, 0xb6,
        0xb6, 0xa0, 0x96, 0x8c, 0xa0, 0xbe, 0x92, 0x9a, 0x8d,
        0x96, 0x9c, 0x9e, 0x91, 0xa0, 0xac, 0x8b, 0x9e, 0x91,
        0x9b, 0x9e, 0x8d, 0x9b, 0xa0, 0xbc, 0x90, 0x9b, 0x9a,
        0xa0, 0x99, 0x90, 0x8d, 0xa0, 0xb6, 0x91, 0x99, 0x90,
        0x8d, 0x92, 0x9e, 0x8b, 0x96, 0x90, 0x91, 0xa0, 0xb6,
        0x91, 0x8b, 0x9a, 0x8d, 0x9c, 0x97, 0x9e, 0x91, 0x98,
        0x9a, 0xa0, 0x82,
    ]

    for key in range(256):  # Try every key from 0 to 255
        decrypted_message = ''.join(chr(xor_decrypt(value, key)) for value in flag_in_ascii)
        
        # Check if the decrypted message is printable
        if is_printable(decrypted_message):
            print(f"Key {key}: {decrypted_message}")

if __name__ == "__main__":
    main()

